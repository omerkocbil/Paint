    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            String secili = "";

            if(eraseWidth == 10) {
                secili = "Küçük";
            }
            else if(eraseWidth == 20) {
                secili = "Orta";
            }
            else if(eraseWidth == 30) {
                secili = "Büyük";
            }
            else if(eraseWidth == 50) {
                secili = "En Büyük";
            }

            String status = (String) JOptionPane.showInputDialog(null, null, "Choose a erase size",
                -1, null, new Object[]{"Küçük", "Orta", "Büyük", "En Büyük"}, secili);

            if(status == "Küçük") {
                eraseWidth = 10;
                eraseHeight = 10;
            }
            else if(status == "Orta") {
                eraseWidth = 20;
                eraseHeight = 20;
            }
            else if(status == "Büyük") {
                eraseWidth = 30;
                eraseHeight = 30;
            }
            else if(status == "En Büyük") {
                eraseWidth = 50;
                eraseHeight = 50;
            }
        }

        silgiMi = true;
        kalemMi = false;
    }//GEN-LAST:event_jButton3MouseClicked

