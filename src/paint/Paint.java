    private void jPanel1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseReleased
        // TODO add your handling code here:
        currentX = evt.getX();
        currentY = evt.getY() + 70;
        
        g2.setColor(new Color(color.get(0)));
        g2.setStroke(new BasicStroke(font.get(0)));
        
        if(kalemMi) {
            if(ovalMi) {
                g2.drawOval(oldX, oldY, currentX - oldX, currentY - oldY);
            }
            else if(dikdortgenMi) {
                g2.drawRect(oldX, oldY, currentX - oldX, currentY - oldY);
            }
            else if(cemberMi) {
                g2.drawOval(oldX, oldY, currentX - oldX, currentX - oldX);
            }
            else if(kareMi) {
                g2.drawRect(oldX, oldY, currentX - oldX, currentX - oldX);
            }
        }
    }//GEN-LAST:event_jPanel1MouseReleased

